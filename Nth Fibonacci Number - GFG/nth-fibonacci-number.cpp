class Solution {
  public:
    long long int nthFibonacci(long long int n){
        // code here
        long long int i=0,j=1,c;
        n=n-1;
        while(n--){
            c=(i+j)%1000000007;
            i=j;
            j=c;
            
        }
        return c;
    }
};


class Solution
{
	public:
		int binary_to_decimal(string str)
		{
		    // Code here.
		    int t=0;
		    int size=str.length();
		    for(int i=0;i<size;i++){
		        if(str[i]=='1')t+=(1<<(size-1-i));
		    }return t;
		}
};


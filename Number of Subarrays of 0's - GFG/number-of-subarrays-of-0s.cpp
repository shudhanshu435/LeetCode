long long int no_of_subarrays(int n, vector<int> &arr) {
    // Write your code here.
    long long int sum=0,zero=0;
    for(int i=0;i<n;i++){
        if(arr[i]==0)zero++;
        else{
            sum+=zero*(zero+1)/2;
            zero=0;
        }
    }
    sum+=zero*(zero+1)/2;
    return sum;
}


class Solution {
public:
    int solve(int a, int b, int c) {
        // code here
        int m=max(a,max(b,c));
        int div=m/2;
        if(m&1)div++;
        if(((a+b+c)-m+1)<div)return -1;
        return a+b+c;
    }
};


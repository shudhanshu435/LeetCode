class Solution{
    public:
    double MedianOfArrays(vector<int>& array1, vector<int>& array2)
    {
        // Your code goes here
        int m=array1.size();
        int n=array2.size();
        for(int i=0;i<m;i++){
            array2.push_back(array1[i]);
        }
        sort(array2.begin(),array2.end());
        if((m+n)&1)return array2[(m+n)/2];
        else{
            double f;
            int g=(m+n)/2;
            f=(array2[g-1]+array2[g])/2.00;
            return f;
        }
    
    }
};

